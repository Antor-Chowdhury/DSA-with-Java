public class DataTypes {
    public static void main(String args[]) {
        // Primitive Data Types

        byte b = 8;
        System.out.println(b);
        char ch = 'a';
        System.out.println(ch);
        boolean var = true;
        double price = 10.5;
        int number = 25;
        // long --> used when the int number is too long
        // double --> used when the float number is too long
        short n = 240;
    }
}